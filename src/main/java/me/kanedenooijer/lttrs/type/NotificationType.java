package me.kanedenooijer.lttrs.type;

/**
 * Enumeration representing the type of notification.
 */
public enum NotificationType {
    /**
     * Indicates a successful operation.
     */
    SUCCESS,

    /**
     * Indicates a warning or potential issue that may require attention.
     */
    WARNING,

    /**
     * Indicates an error or failure that occurred during an operation.
     */
    ERROR
}
